// Build each member's Live post history: a deterministic slice of the pool, scaled to
// how active they are, personalised with their name + derived role. No flags reference
// these ids, so the profile renders them as "Live".
function buildLivePosts(): Content[] {
  const out: Content[] = [];
  const poolLen = LIVE_POST_TEMPLATES.length;
  users.forEach((u, idx) => {
    const total = u.activity.parleys + u.activity.articles + u.activity.takes;
    const count = Math.max(3, Math.min(6, 3 + Math.floor(total / 90)));
    const role = u.profile?.headline ?? deriveProfile(u).headline;
    for (let j = 0; j < count; j++) {
      const t = LIVE_POST_TEMPLATES[(idx * 5 + j * 7) % poolLen];
      out.push({
        id: `lp-${u.id}-${j + 1}`,
        type: t.type,
        authorId: u.id,
        authorName: u.name,
        authorRole: role,
        verified: u.verification === "verified",
        excerpt: t.excerpt,
        body: t.body,
        ...(t.title ? { title: t.title } : {}),
        ...(t.readTime ? { readTime: t.readTime } : {}),
        ...(t.imageUrl ? { imageUrl: t.imageUrl } : {}),
        ...(t.videoThumbnail ? { videoThumbnail: t.videoThumbnail } : {}),
        ...(t.duration ? { duration: t.duration } : {}),
        likes: t.likes + (idx % 11),
        comments: t.comments + (idx % 5),
        shares: t.shares + (idx % 3),
        // Newest post is hours old; the rest spread back over recent weeks.
        createdAt: j === 0 ? hrs(6 + (idx % 18)) : days(j * 4 + (idx % 9)),
        surface: "general",
        deletedAt: null,
        hiddenAt: null,
      });
    }
  });
  return out;
}

